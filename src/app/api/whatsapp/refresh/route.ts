import { NextResponse } from 'next/server';
import { WhatsAppService } from '../../../../lib/whatsapp-service';

// Marcar la ruta como din√°mica para evitar la compilaci√≥n est√°tica
export const dynamic = 'force-dynamic';

export async function POST(request: Request) {
  try {
    // Obtener sessionId del header
    const sessionId = request.headers.get('X-Session-Id') || 'default';
    
    console.log(`üîÑ API: Refrescando cliente WhatsApp para sesi√≥n ${sessionId}...`);
    
    const whatsappService = WhatsAppService.getInstance(sessionId);
    await whatsappService.refreshClient();
    
    // Obtener el estado despu√©s del refresh
    const status = whatsappService.getStatus();
    
    console.log(`‚úÖ API: Cliente refrescado para sesi√≥n ${sessionId}`);
    
    return NextResponse.json({
      success: true,
      message: 'Cliente WhatsApp refrescado',
      status: {
        isConnected: status.isConnected,
        phoneNumber: status.phoneNumber
      }
    });
    
  } catch (error) {
    console.error('‚ùå API Error refrescando cliente:', error);
    return NextResponse.json(
      { success: false, error: error instanceof Error ? error.message : 'Error desconocido' },
      { status: 500 }
    );
  }
} 